    private void emp_update_deduction_calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_emp_update_deduction_calculateActionPerformed
        // TODO add your handling code here:
        int salary = Integer.parseInt(txt_basicsalary4.getText());
        
         if(r_percentage2.isSelected()== true){
        int percentage = Integer.parseInt(txt_d3_percentage4.getText());
        //calculate the total hours of overtime
        int total_percentage_deduction = salary /100 * percentage;
        String x = String.valueOf(total_percentage_deduction);
        int sal = salary - total_percentage_deduction;
        lbl_deduction4.setText(x);
        lbl_afterdeduction4.setText(String.valueOf(sal));
         }
        
        if(r_amount2.isSelected()== true){
        int deduction = Integer.parseInt(txt_d4_amount4.getText());
        //calculate the total hours of overtime
        int total_amount_deduction =  salary - deduction;
        String s = String.valueOf(total_amount_deduction);
       
        lbl_afterdeduction4.setText(s);
        lbl_deduction4.setText(String.valueOf(deduction));
        } 

    }//GEN-LAST:event_emp_update_deduction_calculateActionPerformed

